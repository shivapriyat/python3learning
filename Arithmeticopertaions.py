#!/usr/bin/env python3
x=5
y=3
add=x+y
sub=x-y
mul=x*y
floatdiv=x/y
intdiv=x//y
modulo=x%y
print(f'add of {x} and {y} is {add}')
print(f'sub of {x} and {y} is {sub}')
print(f'mul of {x} and {y} is {mul}')
print(f'floatdiv of {x} and {y} is {floatdiv}')
print(f'intdiv of {x} and {y} is {intdiv}')
print(f'modulo of {x} and {y} is {modulo}')
